def get_city(slug):
    """Zoek een stad op basis van slug."""
    return next((c for c in CITIES if c["slug"] == slug), None)

def get_unpublished_cities(published_slugs):
    """Geef steden terug die nog niet gepubliceerd zijn."""
    return [c for c in CITIES if c["slug"] not in published_slugs]

def get_next_city(published_slugs):
    """Geef de volgende ongepubliceerde stad terug (prioriteitsvolgorde)."""
    remaining = get_unpublished_cities(published_slugs)
    return remaining[0] if remaining else None
